export function detectClientLang() {
  const sources = [telegramLanguage(), navigator.language, ...(navigator.languages || [])].filter(Boolean);
  return sources.some(value => String(value).toLowerCase().startsWith('ru')) ? 'ru' : 'en';
}

export function text(lang, key, ...args) {
  const value = (TEXT[lang] || TEXT.en)[key] ?? TEXT.en[key] ?? key;
  return typeof value === 'function' ? value(...args) : value;
}

export function applyTranslations(lang) {
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const item = STATIC_KEYS[el.dataset.i18n];
    if (item) el.textContent = item[lang] || item.en;
  });
}
